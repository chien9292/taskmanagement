    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'name' => ['required', 'max:200', Rule::unique('groups')->ignore($this->route('group'))],
            'manager_id' => ['exists:users,id']
        ];
    }
}
